export function getDailyStudyIndex(dayKey) {
  const parts = dayKey.split('-').map(Number)
  if (parts.length !== 3 || parts.some((n) => !Number.isFinite(n))) return 0
  const [y, mo, d] = parts
  const epochDay = Math.floor(Date.UTC(y, mo - 1, d) / 86400000)
  const n = DAILY_STUDY_PASSAGES.length
  return ((epochDay % n) + n) % n
}

export function getDailyStudy(dayKey) {
  return DAILY_STUDY_PASSAGES[getDailyStudyIndex(dayKey)]
}

export function getDailyPrayer(dayKey) {
  const i = getDailyStudyIndex(dayKey)
  return DAILY_PRAYERS[i % DAILY_PRAYERS.length]
}
